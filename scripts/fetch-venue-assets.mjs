#!/usr/bin/env node
/**
 * Fetches swing club logos from public homepages (og:image, twitter:image, or favicon).
 * Respects a simple robots.txt check, uses a polite delay between requests, and writes:
 * - public/images/swing-clubs/{slug}-logo.{ext}
 * - src/data/swingClubs/asset-overrides.generated.js
 *
 * Run from repo root: npm run fetch:venue-assets
 */

import { writeFile, mkdir } from 'fs/promises'
import { dirname, join } from 'path'
import { fileURLToPath } from 'url'
import { getAllSwingClubs } from '../src/data/swingClubs.js'

const __dirname = dirname(fileURLToPath(import.meta.url))
const OUT_DIR = join(__dirname, '..', 'public', 'images', 'swing-clubs')
const OVERRIDES_FILE = join(__dirname, '..', 'src', 'data', 'swingClubs', 'asset-overrides.generated.js')

const UA =
  'EastCoastKinkEventsDirectory/1.0 (+https://www.eastcoastkinkevents.com/contact)'

function delay(ms) {
  return new Promise((r) => setTimeout(r, ms))
}

function resolveUrl(base, href) {
  try {
    return new URL(href, base).href
  } catch {
    return null
  }
}

function pickImageUrl(html, pageUrl) {
  const og =
    html.match(/property=["']og:image["'][^>]*content=["']([^"']+)["']/i) ||
    html.match(/content=["']([^"']+)["'][^>]*property=["']og:image["']/i)
  if (og?.[1]) return resolveUrl(pageUrl, og[1].trim())
  const tw = html.match(/name=["']twitter:image["'][^>]*content=["']([^"']+)["']/i)
  if (tw?.[1]) return resolveUrl(pageUrl, tw[1].trim())
  const icon =
    html.match(/<link[^>]+rel=["'][^"']*icon[^"']*["'][^>]*href=["']([^"']+)["']/i) ||
    html.match(/<link[^>]+href=["']([^"']+)["'][^>]*rel=["'][^"']*icon[^"']*["']/i)
  if (icon?.[1]) return resolveUrl(pageUrl, icon[1].trim())
  return null
}

function extFromType(ct) {
  if (!ct) return 'png'
  if (ct.includes('jpeg') || ct.includes('jpg')) return 'jpg'
  if (ct.includes('png')) return 'png'
  if (ct.includes('webp')) return 'webp'
  if (ct.includes('gif')) return 'gif'
  if (ct.includes('svg')) return 'svg'
  return 'png'
}

async function siteBlocksAllCrawlers(origin) {
  try {
    const r = await fetch(`${origin}/robots.txt`, { headers: { 'User-Agent': UA } })
    if (!r.ok) return false
    const t = await r.text()
    if (/^\s*Disallow:\s*\/\s*$/m.test(t) && !/Allow:\s*\//i.test(t)) return true
  } catch {
    /* ignore */
  }
  return false
}

async function main() {
  await mkdir(OUT_DIR, { recursive: true })
  const clubs = getAllSwingClubs()
  const overrides = {}
  for (const club of clubs) {
    const url = club.website
    if (!url || typeof url !== 'string') {
      console.log(`skip ${club.slug}: no website`)
      continue
    }
    let pageUrl
    try {
      pageUrl = new URL(url)
    } catch {
      console.log(`skip ${club.slug}: bad URL`)
      continue
    }
    const origin = pageUrl.origin
    if (await siteBlocksAllCrawlers(origin)) {
      console.log(`skip ${club.slug}: robots.txt blocks site`)
      continue
    }
    await delay(1500)
    let res
    try {
      res = await fetch(url, { redirect: 'follow', headers: { 'User-Agent': UA, Accept: 'text/html' } })
    } catch (e) {
      console.error(`fetch fail ${club.slug}:`, e.message)
      continue
    }
    if (!res.ok) {
      console.log(`HTTP ${res.status} ${club.slug}`)
      continue
    }
    const html = await res.text()
    const finalUrl = res.url || url
    const imgUrl = pickImageUrl(html, finalUrl)
    if (!imgUrl) {
      console.log(`no image candidate ${club.slug}`)
      continue
    }
    await delay(800)
    let imgRes
    try {
      imgRes = await fetch(imgUrl, { headers: { 'User-Agent': UA } })
    } catch (e) {
      console.error(`img fetch ${club.slug}:`, e.message)
      continue
    }
    if (!imgRes.ok) {
      console.log(`image HTTP ${imgRes.status} ${club.slug}`)
      continue
    }
    const buf = Buffer.from(await imgRes.arrayBuffer())
    if (buf.length < 200) {
      console.log(`tiny payload ${club.slug}`)
      continue
    }
    const ext = extFromType(imgRes.headers.get('content-type'))
    const file = `${club.slug}-logo.${ext}`
    const relLogo = `/images/swing-clubs/${file}`
    await writeFile(join(OUT_DIR, file), buf)
    overrides[club.slug] = {
      logo: relLogo,
      assetSourceUrl: imgUrl,
    }
    console.log(`OK ${club.slug} -> ${relLogo}`)
  }

  const body = `/**
 * Auto-generated by scripts/fetch-venue-assets.mjs — logo paths from public club pages.
 * Re-run: npm run fetch:venue-assets
 */
export const ASSET_OVERRIDES = ${JSON.stringify(overrides, null, 2)}
`
  await writeFile(OVERRIDES_FILE, body, 'utf8')
  console.log('Wrote', OVERRIDES_FILE)
}

main().catch((e) => {
  console.error(e)
  process.exit(1)
})
